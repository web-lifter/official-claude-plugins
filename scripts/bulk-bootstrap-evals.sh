#!/usr/bin/env bash
# Bulk-bootstrap missing evals/suite.yaml + evals/iteration-log.md for every
# SKILL.md in the repo that lacks them.
#
# Strategy: skip running the full skill-eval-bootstrap skill on each target
# (too expensive). Instead, read each SKILL.md's frontmatter directly and
# template a seed suite.yaml that:
#   - has 3 activation-positive (canonical, paraphrase, name-mention)
#   - 2 activation-negative (unrelated, near-miss generic)
#   - 1 functional placeholder pointing at the first examples/ file
#   - 2 edge-cases (empty-argument, target-not-found)
# Generated suites are SEEDS — every judge_criteria block needs human review.
#
# Usage:
#   bash scripts/bulk-bootstrap-evals.sh           # generate seeds + review checklist
#   bash scripts/bulk-bootstrap-evals.sh --dry-run # list missing only
#
# Output:
#   - One <skill>/evals/suite.yaml per missing skill (with a header noting seed status)
#   - One <skill>/evals/iteration-log.md per missing skill (if absent)
#   - .anthril/reports/bulk-bootstrap-review.md — review checklist grouped by category

set -eu

REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
cd "$REPO_ROOT"

DRY_RUN=0
[ "${1:-}" = "--dry-run" ] && DRY_RUN=1

TODAY="$(date '+%Y-%m-%d')"
REVIEW_DIR=".anthril/reports"
REVIEW_FILE="$REVIEW_DIR/bulk-bootstrap-review.md"
mkdir -p "$REVIEW_DIR"

# Discover all SKILL.md files.
mapfile -t ALL_SKILLS < <(find . -name SKILL.md -not -path './node_modules/*' -not -path './.git/*' 2>/dev/null | sort)

# Filter to those missing evals/suite.yaml.
MISSING=()
for SK in "${ALL_SKILLS[@]}"; do
    DIR="$(dirname "$SK")"
    if [ ! -f "$DIR/evals/suite.yaml" ]; then
        MISSING+=("$DIR")
    fi
done

echo "Total skills: ${#ALL_SKILLS[@]}"
echo "Missing evals: ${#MISSING[@]}"

if [ "$DRY_RUN" = 1 ]; then
    printf '  %s\n' "${MISSING[@]}"
    exit 0
fi

# Start the review checklist.
{
    echo "# Bulk Bootstrap Review Checklist"
    echo ""
    echo "Generated: $TODAY"
    echo ""
    echo "${#MISSING[@]} seed eval suites were generated. Each requires human tuning before relying on the harness output. Review priorities, in order:"
    echo ""
    echo "1. **judge_criteria** — replace placeholder text with skill-specific assertions"
    echo "2. **functional user_input** — replace \`<replace-with-real-input>\` with a realistic invocation"
    echo "3. **edge cases** — verify the expected_error codes match what the skill actually emits"
    echo ""
    echo "---"
    echo ""
} > "$REVIEW_FILE"

# Group findings by category.
declare -A CATEGORY_LIST
for DIR in "${MISSING[@]}"; do
    CAT="$(echo "$DIR" | awk -F/ '{print $2}')"
    CATEGORY_LIST[$CAT]="${CATEGORY_LIST[$CAT]:-}$DIR"$'\n'
done

GENERATED=0
SKIPPED=0

for CAT in $(echo "${!CATEGORY_LIST[@]}" | tr ' ' '\n' | sort); do
    {
        echo "## Category: $CAT"
        echo ""
    } >> "$REVIEW_FILE"

    while IFS= read -r DIR; do
        [ -z "$DIR" ] && continue
        SKILL_NAME="$(basename "$DIR")"
        SKILL_MD="$DIR/SKILL.md"

        # Extract frontmatter values via python for safe parsing.
        FM_JSON="$(python3 - "$SKILL_MD" <<'PYEOF'
import sys, re, json
text = open(sys.argv[1], 'r', encoding='utf-8', errors='replace').read()
m = re.match(r'^---\n(.*?)\n---\n', text, re.DOTALL)
out = {}
if m:
    for line in m.group(1).splitlines():
        mm = re.match(r'^([A-Za-z_][A-Za-z0-9_-]*):\s*(.*)$', line)
        if mm:
            k, v = mm.group(1), mm.group(2).strip()
            if v.startswith(('"', "'")) and v.endswith(('"', "'")):
                v = v[1:-1]
            out[k] = v
print(json.dumps(out))
PYEOF
)"
        NAME="$(echo "$FM_JSON" | python3 -c "import sys,io,json; sys.stdout=io.TextIOWrapper(sys.stdout.buffer,encoding='utf-8'); d=json.loads(sys.stdin.read()); print(d.get('name',''))")"
        DESC="$(echo "$FM_JSON" | python3 -c "import sys,io,json; sys.stdout=io.TextIOWrapper(sys.stdout.buffer,encoding='utf-8'); d=json.loads(sys.stdin.read()); print(d.get('description',''))")"

        [ -z "$NAME" ] && NAME="$SKILL_NAME"

        # First verb in the description for the paraphrase trigger.
        FIRST_WORD="$(echo "$DESC" | awk '{print $1}')"

        # Has an example?
        FIRST_EXAMPLE=""
        if [ -d "$DIR/examples" ]; then
            FIRST_EXAMPLE="$(find "$DIR/examples" -maxdepth 1 -type f -name '*.md' | head -1)"
        fi

        mkdir -p "$DIR/evals"
        SUITE="$DIR/evals/suite.yaml"
        LOG="$DIR/evals/iteration-log.md"

        if [ -f "$SUITE" ]; then
            SKIPPED=$((SKIPPED+1))
            continue
        fi

        # Generate the seed suite.
        {
            echo "# Auto-generated by bulk-bootstrap-evals.sh on $TODAY."
            echo "# THIS IS A SEED. Review and tune judge_criteria + edge expectations before relying on it."
            echo ""
            echo "skill: $NAME"
            echo "description: \"Seed eval suite generated for $NAME — needs human review\""
            echo ""
            echo "test_cases:"
            echo "  - id: activate-positive-canonical"
            echo "    kind: activation"
            echo "    description: \"should fire on the canonical use case\""
            echo "    user_input: \"$FIRST_WORD this for me\""
            echo "    expected_activation: true"
            echo ""
            echo "  - id: activate-positive-paraphrase"
            echo "    kind: activation"
            echo "    description: \"should fire on paraphrased trigger\""
            echo "    user_input: \"Help me $FIRST_WORD it\""
            echo "    expected_activation: true"
            echo ""
            echo "  - id: activate-positive-name-mention"
            echo "    kind: activation"
            echo "    description: \"should fire when skill mentioned by name\""
            echo "    user_input: \"run $NAME\""
            echo "    expected_activation: true"
            echo ""
            echo "  - id: activate-negative-unrelated"
            echo "    kind: activation"
            echo "    description: \"should NOT fire on unrelated request\""
            echo "    user_input: \"how do I configure SSH key forwarding on Windows?\""
            echo "    expected_activation: false"
            echo ""
            echo "  - id: activate-negative-near-miss"
            echo "    kind: activation"
            echo "    description: \"should NOT fire on a near-miss query (REPLACE with skill-specific near-miss)\""
            echo "    user_input: \"remind me what skills are\""
            echo "    expected_activation: false"
            echo ""
            if [ -n "$FIRST_EXAMPLE" ]; then
                echo "  - id: functional-golden"
                echo "    kind: functional"
                echo "    description: \"produces the artefact described in examples/\""
                echo "    user_input: \"<REPLACE-with-realistic-input-for-$NAME>\""
                echo "    expected_outputs:"
                echo "      - kind: file_created"
                echo "        path_glob: \"*.md\""
                echo "    judge_criteria:"
                echo "      - \"No American spellings present (color, optimize, behavior, organize, center) — Australian or neutral forms both pass\""
                echo "      - \"REPLACE: add skill-specific assertion about required sections / fields\""
                echo "    timeout_seconds: 240"
                echo ""
            fi
            echo "  - id: edge-empty-input"
            echo "    kind: edge-case"
            echo "    description: \"handles empty input gracefully\""
            echo "    user_input: \"\""
            echo "    expected_error: \"empty-argument\""
            echo ""
            echo "  - id: edge-nonexistent-target"
            echo "    kind: edge-case"
            echo "    description: \"handles target that does not exist\""
            echo "    user_input: \"this-target-definitely-does-not-exist-xyz\""
            echo "    expected_error: \"target-not-found\""
        } > "$SUITE"

        if [ ! -f "$LOG" ]; then
            {
                echo "# Iteration Log — $NAME"
                echo ""
                echo "| Date | Pass-rate | Regressions | Wins | New | Mode | Notes |"
                echo "|---|---|---|---|---|---|---|"
                echo "| $TODAY | — | — | — | initial | n/a | Seed suite generated by bulk-bootstrap-evals.sh; awaiting first harness run. |"
            } > "$LOG"
        fi

        GENERATED=$((GENERATED+1))

        # Note in review file.
        REL="$(echo "$DIR" | sed 's|^./||')"
        {
            if [ -n "$FIRST_EXAMPLE" ]; then
                echo "- [ ] **$REL** — has example; tune functional user_input + judge_criteria"
            else
                echo "- [ ] **$REL** — no example; consider adding one before relying on functional tests"
            fi
        } >> "$REVIEW_FILE"
    done <<< "${CATEGORY_LIST[$CAT]}"

    echo "" >> "$REVIEW_FILE"
done

{
    echo "---"
    echo ""
    echo "## Summary"
    echo ""
    echo "- Generated: $GENERATED"
    echo "- Skipped (already had suite): $SKIPPED"
    echo "- Total skills with evals now: $(find . -path '*/evals/suite.yaml' -not -path './node_modules/*' -not -path './.git/*' | wc -l | awk '{print $1}')"
} >> "$REVIEW_FILE"

echo ""
echo "Generated $GENERATED seed suites (skipped $SKIPPED already-populated)."
echo "Review checklist: $REVIEW_FILE"
